    fun getFourCountries(code: String): ArrayList<String> {
        val countries = ArrayList<String>()
        countries.add(getCountryName(code))

        while (countries.size != 4) {
            val randomNum = (0 until countriesCodes.size).random()
            val randomCountryCode = countriesCodes[randomNum]
            val randomCountryName = getCountryName(randomCountryCode)
            if (!countries.contains(randomCountryName)) {
                countries.add(randomCountryName)
            }
        }
        countries.shuffle()
        return countries

    }

