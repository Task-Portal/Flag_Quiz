    fun getQuestions(): ArrayList<Question> {


        val questionsList = ArrayList<Question>()
        countriesCodes.shuffle()
        for ((index, countryCode) in countriesCodes.withIndex()) {
            val fourCountries = getFourCountries(countryCode)
            val flagCountryName = getCountryName(countryCode)
            val question = Question(
                index + 1,
                "Какая страна принадлежит этому флагу?",
                R.drawable::class.java.getDeclaredField(countryCode).getInt(null),
                fourCountries[0],
                fourCountries[1],
                fourCountries[2],
                fourCountries[3],
                fourCountries.indexOf(flagCountryName)+1
            )
            questionsList.add(question)
        }

        return questionsList
    }

